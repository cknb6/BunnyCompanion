    public static ChatReply Reply(
        string? userText,
        PetSettings settings,
        bool offlineMode = false,
        bool desktopRequested = false)
    {
        var text = (userText ?? string.Empty).Trim();
        if (text.Length == 0)
        {
            if (desktopRequested)
            {
                return new ChatReply(
                    Format("想帮你看桌面呢…现在先用本地模式陪你。跟我说你在忙什么也好甜。", settings),
                    "curious",
                    1);
            }

            return new ChatReply(
                Format(offlineMode
                    ? "我在听哦～本地陪伴也开着，想说什么都可以。"
                    : "我在听哦，想说什么都可以呀。", settings),
                "curious",
                0);
        }

        // 保留更多上下文，便于长句命中关键词
        if (text.Length > 400)
            text = text[..400];

        foreach (var (keywords, replies, action, affection) in Rules)
        {
            if (!keywords.Any(keyword => MatchesKeyword(text, keyword)))
                continue;

            var reply = replies[Random.Shared.Next(replies.Length)];
            if (offlineMode && Random.Shared.NextDouble() < 0.12)
                reply += "（本地也黏着你）";
            return new ChatReply(Format(reply, settings), action, affection);
        }

        if (settings.LoveMessages.Count > 0 && Random.Shared.NextDouble() < 0.48)
        {
            var love = settings.LoveMessages[Random.Shared.Next(settings.LoveMessages.Count)];
            return new ChatReply(Format(love, settings), "shy", 2);
        }

        var fallback = FallbackReplies[Random.Shared.Next(FallbackReplies.Length)];
        return new ChatReply(Format(fallback, settings), "curious", 1);
    }

    /// <summary>
    /// 中文关键词用子串匹配；短英文词用词边界，避免 hit/this 误触发。
    /// </summary>
    private static bool MatchesKeyword(string text, string keyword)
    {
        if (string.IsNullOrEmpty(keyword))
            return false;

        var isAsciiWord = keyword.All(c => c <= 127) && keyword.Any(char.IsLetter);
        if (isAsciiWord && keyword.Length <= 8)
        {
            var idx = 0;
            while (idx < text.Length)
            {
                var found = text.IndexOf(keyword, idx, StringComparison.OrdinalIgnoreCase);
                if (found < 0)
                    return false;
                var beforeOk = found == 0 || !char.IsLetterOrDigit(text[found - 1]);
                var after = found + keyword.Length;
                var afterOk = after >= text.Length || !char.IsLetterOrDigit(text[after]);
                if (beforeOk && afterOk)
                    return true;
                idx = found + 1;
            }
            return false;
        }

        return text.Contains(keyword, StringComparison.OrdinalIgnoreCase);
    }

    private static string Format(string message, PetSettings settings) => message
        .Replace("{name}", settings.PartnerName, StringComparison.OrdinalIgnoreCase)
        .Replace("{pet}", settings.PetName, StringComparison.OrdinalIgnoreCase)
        .Replace("{{name}}", settings.PartnerName, StringComparison.OrdinalIgnoreCase)
        .Replace("{{pet}}", settings.PetName, StringComparison.OrdinalIgnoreCase);
