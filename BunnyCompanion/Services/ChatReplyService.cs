    public static ChatReply Reply(
        string? userText,
        PetSettings settings,
        bool offlineMode = false,
        bool desktopRequested = false)
    {
        var text = (userText ?? string.Empty).Trim();
        if (text.Length == 0)
        {
            if (desktopRequested)
            {
                return new ChatReply(
                    Format("我想看桌面，但现在只能本地陪你。也可以用中文告诉我你屏幕上在忙什么。", settings),
                    "curious",
                    1);
            }

            return new ChatReply(
                Format(offlineMode
                    ? "我在听哦（本地陪伴模式）。想说什么都可以。"
                    : "我在听哦，想说什么都可以。", settings),
                "curious",
                0);
        }

        if (text.Length > 200)
            text = text[..200];

        var lowered = text.ToLowerInvariant();
        foreach (var (keywords, replies, action, affection) in Rules)
        {
            if (!keywords.Any(keyword =>
                    text.Contains(keyword, StringComparison.OrdinalIgnoreCase)
                    || lowered.Contains(keyword.ToLowerInvariant())))
                continue;

            var reply = replies[Random.Shared.Next(replies.Length)];
            return new ChatReply(Format(reply, settings), action, affection);
        }

        if (settings.LoveMessages.Count > 0 && Random.Shared.NextDouble() < 0.40)
        {
            var love = settings.LoveMessages[Random.Shared.Next(settings.LoveMessages.Count)];
            return new ChatReply(Format(love, settings), "shy", 1);
        }

        var fallback = FallbackReplies[Random.Shared.Next(FallbackReplies.Length)];
        return new ChatReply(Format(fallback, settings), "curious", 1);
    }

    private static string Format(string message, PetSettings settings) => message
        .Replace("{name}", settings.PartnerName, StringComparison.OrdinalIgnoreCase)
        .Replace("{pet}", settings.PetName, StringComparison.OrdinalIgnoreCase)
        .Replace("{{name}}", settings.PartnerName, StringComparison.OrdinalIgnoreCase)
        .Replace("{{pet}}", settings.PetName, StringComparison.OrdinalIgnoreCase);
